<template>
  <div>
    <!--新增弹框-->
    <el-dialog title="用户信息" :visible.sync="dialogFormVisible" size="small" width="30%">
      <el-form :model="form" label-width="80px">
        <el-form-item label="名称">
          <el-input v-model="form.name" autocomplete="off"></el-input>
        </el-form-item>
        <el-form-item label="路径">
          <el-input v-model="form.path" autocomplete="off"></el-input>
        </el-form-item>
        <el-form-item label="页面路径">
          <el-input v-model="form.pagePath" autocomplete="off"></el-input>
        </el-form-item>
        <el-form-item label="图标">
          <el-select clearable v-model="form.icon" placeholder="请选择" style="width: 100%">
            <el-option v-for="item in options" :key="item.name" :label="item.name" :value="item.value">
              <i :class="item.value"></i> {{item.name }}
            </el-option>
          </el-select>
        </el-form-item>

        <el-form-item label="顺序">
          <el-input v-model="form.sortNum" autocomplete="off"></el-input>
        </el-form-item>
        <el-form-item label="描述">
          <el-input v-model="form.description" autocomplete="off"></el-input>
        </el-form-item>
      </el-form>
      <div slot="footer" class="dialog-footer">
        <el-button @click="dialogFormVisible = false">取 消</el-button>
        <el-button type="primary" @click="save()">确 定</el-button>
      </div>
    </el-dialog>



    <div style="margin: 10px 0">
      <el-input style="width: 200px" placeholder="请输入名称" suffix-icon="el-icon-search" v-model="name"></el-input>
      <el-input style="width: 200px" placeholder="请输入描述信息" suffix-icon="el-icon-massage" class="ml-5"
                v-model="description"></el-input>
      <el-button class="ml-5" type="primary" @click="load()">搜索</el-button>
      <el-button class="ml-5" type="danger" @click="reset()">重置</el-button>
    </div>
    <div style="margin: 10px 0">

      <el-button type="primary" @click="handleAdd()">新增 <i class="el-icon-circle-plus-outline "></i></el-button>
      <el-popconfirm

          class="ml-5"
          confirm-button-text='确定'
          cancel-button-text='我再想想'
          icon="el-icon-info"
          icon-color="red"
          title="您确定删除吗？"
          @confirm="delBatch()"
      >
        <el-button type="danger" slot="reference">批量删除 <i class="el-icon-remove-outline "></i></el-button>
      </el-popconfirm>
    </div>
    <el-table
        :data="tableData"
        style="width: 100%"
        row-key="id"
        default-expand-all
        :row-class-name="tableRowClassName" border
        @selection-change="handleSelectionChange">

      <el-table-column
          type="selection"
      >
      </el-table-column>
      <el-table-column
          prop="id"
          label="ID"
          width="80px"
      >
      </el-table-column>
      <el-table-column
          prop="name"
          label="名称"
      >
      </el-table-column>
      <el-table-column
          prop="path"
          label="路径"
      >
      </el-table-column>
      <el-table-column
          prop="pagePath"
          label="页面路径"
      >
      </el-table-column>
      <el-table-column
          label="图标"
          align="center"
          class-name="fontSize18"
          label-class-name="fontSize12"
      >
        <template slot-scope="scope">
          <i :class="scope.row.icon" />
        </template>
      </el-table-column>
      <el-table-column
          prop="description"
          label="描述信息"
      >
      </el-table-column>
      <el-table-column prop="sortNum" label="顺序"></el-table-column>
      <el-table-column label="操作" width="280" align="center">
        <template slot-scope="scope">
          <el-button type="primary" @click="addChildren(scope.row.id)" v-if="!scope.row.pid">新增子菜单 <i class="el-icon-circle-plus-outline "></i></el-button>
          <el-button type="success" @click="handleEdit(scope.row)">编辑<i class="el-icon-edit"></i></el-button>
          <el-popconfirm
              class="ml-5"
              confirm-button-text='确定'
              cancel-button-text='我再想想'
              icon="el-icon-info"
              icon-color="red"
              title="您确定删除吗？"
              @confirm="del(scope.row.id)"
          >
            <el-button type="danger" slot="reference">删除<i class="el-icon-remove-outline"></i></el-button>
          </el-popconfirm>
        </template>
      </el-table-column>
    </el-table>
  </div>
</template>

<script>
export default {
  name: "Menu",
  data() {
    return {
      name: '',
      description: '',
      path: '',
      icon: '',
      form: {},
      multipleSelection: [],
      dialogFormVisible: false,
      tableData: [],
      options: [],
    }
  },
  created() {
   this.load()
  },
  methods: {
    tableRowClassName({row, rowIndex}) {
      if (rowIndex % 4 == 1) {
        return 'warning-row';
      } else if (rowIndex % 4 == 3) {
        return 'success-row';
      }
      return '';
    },
    load() {
      this.request.get("http://localhost:9090/menu", {
        params: {
          name: this.name,
        }
      }).then((res) => {

        //console.log(res);
        this.tableData = res.data;

      })

    },
    reset() {
      this.name = ''
      this.description = ''
      this.load()
    },
    save() {
      this.request.post("http://localhost:9090/menu", this.form).then(res => {
        if (res) {
          this.$message.success("保存成功")
          this.dialogFormVisible = false
          this.load()
        } else {
          this.$message.error("保存失败")
        }

      })

    },

    addChildren(pid){
      this.dialogFormVisible = true
      this.form = {}
      //请求图标的数据
      this.request.get("http://localhost:9090/menu/icons").then((res) => {
        this.options = res.data; })
      if (pid){
        this.form.pid = pid
      }
    },
    handleAdd() {
      this.dialogFormVisible = true
      this.form = {}
      //请求图标的数据
      this.request.get("http://localhost:9090/menu/icons").then((res) => {
            this.options = res.data;
      })
    },
    handleEdit(row) {
      this.form = row
      this.dialogFormVisible = true
      //请求图标的数据
      this.request.get("http://localhost:9090/menu/icons").then((res) => {
        this.options = res.data;
      })

    },

    del(id) {
      this.request.delete("http://localhost:9090/menu/" + id).then(res => {
        if (res) {
          this.$message.success("删除成功")
          this.load()
        } else {
          this.$message.error("删除失败")
        }
      })
    },
    handleSelectionChange(val) {
      this.multipleSelection = val
    },
    delBatch() {
      let ids = this.multipleSelection.map(v => v.id) //[{} ,{} ,{}] => [1,2,3]
      this.request.post("http://localhost:9090/menu/del/batch", ids).then(res => {
        if (res) {
          this.$message.success("批量删除成功")
          this.load()
        } else {
          this.$message.error("批量删除失败")
        }
      })
    },

  }
}
</script>

